public class Rana {
    private String Color;
    private String Dueño;

    public Rana(String color, String dueño) {
        Color = color;
        Dueño = dueño;
    }

    public String getColor() {
        return Color;
    }

    public void setColor(String color) {
        Color = color;
    }

    public String getDueño() {
        return Dueño;
    }

    public void setDueño(String dueño) {
        Dueño = dueño;
    }

}
